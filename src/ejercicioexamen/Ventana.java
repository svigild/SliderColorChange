    private void cambiarColor () {
        //Primero obtenemos el color actual
        Color color = new Color (jSliderRojo.getValue(), jSliderVerde.getValue(), jSliderAzul.getValue());
        //Si est√° marcado el fondo, se cambia el fondo
        if (jRadioButtonFondo.isSelected()){
            jLabelTexto.setBackground(color);
        }
        if (!jRadioButtonFondo.isSelected()){
            jLabelTexto.setForeground(color);
        }
        
   
    }
    
